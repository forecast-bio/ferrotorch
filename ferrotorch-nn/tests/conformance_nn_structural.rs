/// conformance_nn_structural: ferrotorch_nn::Sequential nested forward
#[test]
fn container_sequential_nested_forward_matches_fixture() {
    let fixtures = fixtures_json();
    let fx = fixture_by_id(&fixtures, "sequential_nested_forward");

    // Build: Linear(4->3, W=0.1) -> IdentityScaled (proxy for ReLU) -> Linear(3->2, W=0.2).
    // We use the actual Linear layer from ferrotorch-nn and compare to fixture.
    let mut lin1 = Linear::<f32>::new(4, 3, false).unwrap();
    let mut lin2 = Linear::<f32>::new(3, 2, false).unwrap();

    // Set weights to match the fixture (all 0.1 / 0.2).
    lin1.weight = Parameter::from_slice(&[0.1f32; 12], &[3, 4]).unwrap();
    lin2.weight = Parameter::from_slice(&[0.2f32; 6], &[2, 3]).unwrap();

    // Sequential: lin1 -> lin2 (no ReLU, so output is just chained linear).
    let seq = Sequential::new(vec![
        Box::new(lin1) as Box<dyn Module<f32>>,
        Box::new(lin2) as Box<dyn Module<f32>>,
    ]);

    let x = Tensor::from_storage(
        TensorStorage::cpu(vec![1.0f32; 8]),
        vec![2, 4],
        false,
    ).unwrap();

    let output = seq.forward(&x).unwrap();

    assert_eq!(output.shape(), &[2, 2]);

    // Verify against fixture expected.
    let expected = json_to_f32_flat(&fx["expected"]["output"]);
    let tol = 1e-5f32;
    assert_close_f32(output.data().unwrap(), &expected, tol, "sequential_nested_forward");
}

