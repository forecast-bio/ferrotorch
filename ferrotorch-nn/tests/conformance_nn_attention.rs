/// Layer-3 test: MultiheadAttention forward — multiple dtype/GQA/causal variants.
#[test]
fn mha_forward_matches_reference() {
    let fixtures = load_fixtures();
    let mha_fixtures: Vec<_> = fixtures
        .iter()
        .filter(|f| f["op"] == "mha_forward")
        .collect();

    assert!(
        !mha_fixtures.is_empty(),
        "No mha_forward fixtures found — re-run regenerate_nn_attention_fixtures.py"
    );

    for fix in mha_fixtures {
        let tag = extract_str(fix, "tag");
        let dtype = extract_str(fix, "dtype");
        let batch = extract_usize(fix, "batch");
        let seq = extract_usize(fix, "seq");
        let ed = extract_usize(fix, "embed_dim");
        let num_heads = extract_usize(fix, "num_heads");
        let num_kv_heads = extract_usize(fix, "num_kv_heads");
        let bias = extract_bool(fix, "bias");
        let causal_mask = extract_bool(fix, "causal_mask");

        let input_data = extract_f64_list(fix, "input_data");
        let expected = extract_f64_list(fix, "expected_output");

        // Only f32 fixtures are tested here; f64 MHA forward is cascade-tracked.
        if dtype != "float32" {
            continue;
        }

        let mut mha = MultiheadAttention::<f32>::with_gqa(ed, num_heads, num_kv_heads, bias)
            .unwrap_or_else(|e| panic!("[{tag}] MHA construction failed: {e}"));

        inject_mha_weights(&mut mha, fix);

        let x = tensor_f32(&input_data, vec![batch, seq, ed]);

        let out = if causal_mask {
            // Use forward_qkv with explicit causal flag.
            mha.forward_qkv(&x, &x, &x, true)
        } else {
            mha.forward(&x)
        }
        .unwrap_or_else(|e| panic!("[{tag}] MHA forward failed: {e}"));

        assert_eq!(out.shape(), &[batch, seq, ed], "[{tag}] output shape");

        tol::assert_close(&data_f32(&out), &expected, tol::F32_MATMUL, tag);
    }
}

