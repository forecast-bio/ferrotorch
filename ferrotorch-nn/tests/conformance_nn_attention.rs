// ── Tolerances ─────────────────────────────────────────────────────────────

mod tol {
    /// F32 matmul-dominated ops (MHA, FlashAttention, encoder/decoder).
    pub const F32_MATMUL: f64 = 1e-4;
    /// F32 pointwise ops (RoPE, SwiGLU, KVCache copy).
    pub const F32_POINTWISE: f64 = 1e-5;
    /// F64 matmul.
    pub const F64_MATMUL: f64 = 1e-9;

    pub fn assert_close(actual: &[f64], expected: &[f64], tol: f64, label: &str) {
        assert_eq!(
            actual.len(),
            expected.len(),
            "{label}: length mismatch (actual={}, expected={})",
            actual.len(),
            expected.len()
        );
        for (i, (&a, &e)) in actual.iter().zip(expected.iter()).enumerate() {
            if a.is_nan() && e.is_nan() {
                continue;
            }
            if !a.is_finite() || !e.is_finite() {
                if a.to_bits() == e.to_bits() {
                    continue;
                }
                if a.is_infinite() && e.is_infinite() && a.signum() == e.signum() {
                    continue;
                }
                panic!("{label}: index {i} non-finite mismatch (actual={a}, expected={e})");
            }
            let diff = (a - e).abs();
            let scale = e.abs().max(1.0);
            let allowed = tol * scale;
            assert!(
                diff <= allowed,
                "{label}: index {i} delta {diff:.3e} > tol {tol:.3e} (actual={a}, expected={e})"
            );
        }
    }
}

