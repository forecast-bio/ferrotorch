struct IdentityScaled<T: Float> {
    weight: Parameter<T>,
    training: bool,
}

impl<T: Float> IdentityScaled<T> {
    fn new(size: usize) -> FerrotorchResult<Self> {
        Ok(Self {
            weight: Parameter::zeros(&[size])?,
            training: true,
        })
    }
}

impl<T: Float> Module<T> for IdentityScaled<T> {
    fn forward(&self, input: &Tensor<T>) -> FerrotorchResult<Tensor<T>> {
        Ok(input.clone())
    }
    fn parameters(&self) -> Vec<&Parameter<T>> { vec![&self.weight] }
    fn parameters_mut(&mut self) -> Vec<&mut Parameter<T>> { vec![&mut self.weight] }
    fn named_parameters(&self) -> Vec<(String, &Parameter<T>)> {
        vec![("weight".into(), &self.weight)]
    }
    fn train(&mut self) { self.training = true; }
    fn eval(&mut self) { self.training = false; }
    fn is_training(&self) -> bool { self.training }
}

// ---------------------------------------------------------------------------
// Helper: module with one parameter AND one buffer + one child
// ---------------------------------------------------------------------------

struct ParentWithBuffer<T: Float> {
    weight: Parameter<T>,
    running_mean: Buffer<T>,
    child: IdentityScaled<T>,
}

impl<T: Float> ParentWithBuffer<T> {
    fn new() -> FerrotorchResult<Self> {
        Ok(Self {
            weight: Parameter::ones(&[2, 2])?,
            running_mean: Buffer::zeros(&[2])?,
            child: IdentityScaled::new(3)?,
        })
    }
}

impl<T: Float> Module<T> for ParentWithBuffer<T> {
    fn forward(&self, input: &Tensor<T>) -> FerrotorchResult<Tensor<T>> {
        self.child.forward(input)
    }
    fn parameters(&self) -> Vec<&Parameter<T>> {
        let mut out: Vec<&Parameter<T>> = vec![&self.weight];
        out.extend(self.child.parameters());
        out
    }
    fn parameters_mut(&mut self) -> Vec<&mut Parameter<T>> {
        let mut out: Vec<&mut Parameter<T>> = vec![&mut self.weight];
        out.extend(self.child.parameters_mut());
        out
    }
    fn named_parameters(&self) -> Vec<(String, &Parameter<T>)> {
        let mut out: Vec<(String, &Parameter<T>)> = vec![("weight".into(), &self.weight)];
        for (n, p) in self.child.named_parameters() {
            out.push((format!("child.{n}"), p));
        }
        out
    }
    fn buffers(&self) -> Vec<&Buffer<T>> { vec![&self.running_mean] }
    fn buffers_mut(&mut self) -> Vec<&mut Buffer<T>> { vec![&mut self.running_mean] }
    fn named_buffers(&self) -> Vec<(String, &Buffer<T>)> {
        vec![("running_mean".into(), &self.running_mean)]
    }
    fn children(&self) -> Vec<&dyn Module<T>> { vec![&self.child] }
    fn named_children(&self) -> Vec<(String, &dyn Module<T>)> {
        vec![("child".into(), &self.child)]
    }
    fn train(&mut self) { self.child.train(); }
    fn eval(&mut self) { self.child.eval(); }
    fn is_training(&self) -> bool { self.child.is_training() }
}

