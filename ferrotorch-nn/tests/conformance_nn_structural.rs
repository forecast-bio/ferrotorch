/// conformance_nn_structural: ferrotorch_nn forward hook fires once per forward call
#[test]
fn hooks_forward_hook_fires_once_per_forward() {
    let m = IdentityScaled::<f32>::new(3).unwrap();
    let hooked = HookedModule::new(m);
    let count = Arc::new(AtomicUsize::new(0));
    let c = Arc::clone(&count);
    let _h = hooked.register_forward_hook(Box::new(move |_in, _out| {
        c.fetch_add(1, Ordering::Relaxed);
    }));

    let x = Tensor::from_storage(TensorStorage::cpu(vec![1.0f32; 3]), vec![3], false).unwrap();
    hooked.forward(&x).unwrap();
    assert_eq!(count.load(Ordering::Relaxed), 1);
    hooked.forward(&x).unwrap();
    assert_eq!(count.load(Ordering::Relaxed), 2);
}

