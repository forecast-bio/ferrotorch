/// conformance_nn_structural: ferrotorch_nn HookHandle::remove stops the hook firing
#[test]
fn hooks_handle_remove_stops_hook() {
    let m = IdentityScaled::<f32>::new(2).unwrap();
    let hooked = HookedModule::new(m);
    let count = Arc::new(AtomicUsize::new(0));
    let c = Arc::clone(&count);
    let handle = hooked.register_forward_hook(Box::new(move |_in, _out| {
        c.fetch_add(1, Ordering::Relaxed);
    }));

    let x = Tensor::from_storage(TensorStorage::cpu(vec![1.0f32; 2]), vec![2], false).unwrap();
    hooked.forward(&x).unwrap();
    assert_eq!(count.load(Ordering::Relaxed), 1);

    handle.remove();

    hooked.forward(&x).unwrap();
    assert_eq!(count.load(Ordering::Relaxed), 1, "hook must not fire after remove");
}

