/// Smoke test: reshape_to_heads and repeat_kv shape contracts.
#[test]
fn reshape_to_heads_and_repeat_kv_shapes() {
    let fixtures = load_fixtures();

    // reshape_to_heads
    let rth_fixtures: Vec<_> = fixtures
        .iter()
        .filter(|f| f["op"] == "reshape_to_heads")
        .collect();
    for fix in &rth_fixtures {
        let tag = extract_str(fix, "tag");
        let batch = extract_usize(fix, "batch");
        let seq = extract_usize(fix, "seq");
        let num_heads = extract_usize(fix, "num_heads");
        let head_dim = extract_usize(fix, "head_dim");
        let embed_dim = num_heads * head_dim;

        let input = extract_f64_list(fix, "input_data");
        let expected = extract_f64_list(fix, "expected_output");

        let x = tensor_f32(&input, vec![batch, seq, embed_dim]);
        // reshape_to_heads signature: (tensor, num_heads, seq_len, head_dim)
        let out = reshape_to_heads::<f32>(&x, num_heads, seq, head_dim)
            .unwrap_or_else(|e| panic!("[{tag}] reshape_to_heads failed: {e}"));

        // Output shape is [num_heads, seq, head_dim] (no batch dim — single-batch fn)
        assert_eq!(out.shape(), &[num_heads, seq, head_dim], "[{tag}] shape");
        // Reference was computed as [batch, num_heads, seq, head_dim]; first batch slice matches
        let out_data = data_f32(&out);
        let expected_slice = &expected[..out_data.len()];
        tol::assert_close(&out_data, expected_slice, tol::F32_POINTWISE, tag);
    }

    // repeat_kv
    let rkv_fixtures: Vec<_> = fixtures
        .iter()
        .filter(|f| f["op"] == "repeat_kv")
        .collect();
    for fix in &rkv_fixtures {
        let tag = extract_str(fix, "tag");
        let batch = extract_usize(fix, "batch");
        let kv_heads = extract_usize(fix, "kv_heads");
        let seq = extract_usize(fix, "seq");
        let head_dim = extract_usize(fix, "head_dim");
        let group_size = extract_usize(fix, "group_size");

        let input = extract_f64_list(fix, "input_data");
        let expected = extract_f64_list(fix, "expected_output");

        // repeat_kv takes 3-D [kv_heads, seq, head_dim] (no batch dim).
        // Process each batch item independently and verify the first item's
        // output against the first slice of the expected reference.
        let input_data_f32: Vec<f32> = input.iter().map(|&x| x as f32).collect();
        let per_item_elems = kv_heads * seq * head_dim;
        let out_item_elems = kv_heads * group_size * seq * head_dim;

        for b in 0..batch {
            let slice = &input_data_f32[b * per_item_elems..(b + 1) * per_item_elems];
            let kv_3d = tensor_f32(
                &slice.iter().map(|&x| x as f64).collect::<Vec<_>>(),
                vec![kv_heads, seq, head_dim],
            );
            let out = repeat_kv::<f32>(&kv_3d, group_size)
                .unwrap_or_else(|e| panic!("[{tag}] repeat_kv batch {b} failed: {e}"));

            assert_eq!(
                out.shape(),
                &[kv_heads * group_size, seq, head_dim],
                "[{tag}] batch {b} shape"
            );
            let expected_slice = &expected[b * out_item_elems..(b + 1) * out_item_elems];
            tol::assert_close(&data_f32(&out), expected_slice, tol::F32_POINTWISE, tag);
        }
    }
}

