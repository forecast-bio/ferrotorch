fn fixtures_json() -> serde_json::Value {
    let path = concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/tests/conformance/fixtures_nn_structural.json"
    );
    let text = std::fs::read_to_string(path).unwrap_or_else(|_| {
        panic!(
            "fixtures_nn_structural.json must exist; run \
             scripts/regenerate_nn_structural_fixtures.py"
        )
    });
    serde_json::from_str(&text).expect("fixtures_nn_structural.json must be valid JSON")
}

fn fixture_by_id(fixtures: &serde_json::Value, id: &str) -> serde_json::Value {
    fixtures["fixtures"]
        .as_array()
        .expect("fixtures array present")
        .iter()
        .find(|f| f["id"] == id)
        .unwrap_or_else(|| panic!("fixture '{}' not found", id))
        .clone()
}

