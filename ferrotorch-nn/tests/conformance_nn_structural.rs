fn assert_close_f32(actual: &[f32], expected: &[f32], tol: f32, label: &str) {
    assert_eq!(
        actual.len(),
        expected.len(),
        "{label}: length mismatch {} vs {}",
        actual.len(),
        expected.len()
    );
    for (i, (&a, &e)) in actual.iter().zip(expected.iter()).enumerate() {
        assert!(
            (a - e).abs() <= tol,
            "{label}[{i}]: actual={a:.7} expected={e:.7} diff={:.2e} tol={tol:.2e}",
            (a - e).abs()
        );
    }
}

fn json_to_f32_flat(v: &serde_json::Value) -> Vec<f32> {
    fn flatten(v: &serde_json::Value, out: &mut Vec<f32>) {
        match v {
            serde_json::Value::Array(arr) => {
                for item in arr {
                    flatten(item, out);
                }
            }
            serde_json::Value::Number(n) => {
                out.push(n.as_f64().unwrap() as f32);
            }
            _ => panic!("unexpected value in json_to_f32_flat: {v}"),
        }
    }
    let mut out = Vec::new();
    flatten(v, &mut out);
    out
}

